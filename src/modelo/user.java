package modelo;

/**
 *
 * @author Cesar
 */
public class user {
    int idUser;
    String name;
    String pass;
    int edad;

    public user(int idUser, String name, String pass, int edad) {
        this.idUser = idUser;
        this.name = name;
        this.pass = pass;
        this.edad = edad;
    }

    public user() {
    }
    
    public int getIdUser() {
        return idUser;
    }

    public void setIdUser(int idUser) {
        this.idUser = idUser;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public String getPass() {
        return pass;
    }

    public void setPass(String pass) {
        this.pass = pass;
    }

    public int getEdad() {
        return edad;
    }

    public void setEdad(int edad) {
        this.edad = edad;
    }
    
    
}
