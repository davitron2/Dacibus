
package modelo;

public class Producto {
   
    private int IdProducto;
    private String NombrePro;
    private String Categoria;
    private int Precio;
    private int CantPro;
    private int CantTenedor;
    private int CantVasos;
    private int CantCucharas;
    private int IdUtensilios;

    public int getIdProducto() {
        return IdProducto;
    }

    public void setIdProducto(int IdProducto) {
        this.IdProducto = IdProducto;
    }

    public String getNombrePro() {
        return NombrePro;
    }

    public void setNombrePro(String NombrePro) {
        this.NombrePro = NombrePro;
    }

    public String getCategoria() {
        return Categoria;
    }

    public void setCategoria(String Categoria) {
        this.Categoria = Categoria;
    }

    public int getPrecio() {
        return Precio;
    }

    public void setPrecio(int Precio) {
        this.Precio = Precio;
    }

    public int getCantPro() {
        return CantPro;
    }

    public void setCantPro(int CantPro) {
        this.CantPro = CantPro;
    }

    public int getCantTenedor() {
        return CantTenedor;
    }

    public void setCantTenedor(int CantTenedor) {
        this.CantTenedor = CantTenedor;
    }

    public int getCantVasos() {
        return CantVasos;
    }

    public void setCantVasos(int CantVasos) {
        this.CantVasos = CantVasos;
    }

    public int getCantCucharas() {
        return CantCucharas;
    }

    public void setCantCucharas(int CantCucharas) {
        this.CantCucharas = CantCucharas;
    }

    public int getIdUtensilios() {
        return IdUtensilios;
    }

    public void setIdUtensilios(int IdUtensilios) {
        this.IdUtensilios = IdUtensilios;
    }
    
    
}
